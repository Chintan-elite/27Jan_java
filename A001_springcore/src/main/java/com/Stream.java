package com;

public class Stream {
	
	String code;
	String Name;
	
	public void display()
	{
		System.out.println(code+" "+Name);
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}
	
	
}
